def list_model_families() -> dict[str, list[str]]:
    families: dict[str, list[str]] = {}
    for record in list_model_integrations():
        families.setdefault(record.family, []).append(record.model_id)
    return families


def model_registry_payload() -> dict[str, Any]:
    rows = list_model_integrations()
    return {
        "status": "ok",
        "model_count": len(rows),
        "families": list_model_families(),
        "models": [row.as_dict() for row in rows],
        "limitations": [
            "Registry describes integration intent and safety defaults only.",
            "No model is implicitly enabled or downloaded.",
            "Availability remains unavailable until local config explicitly enables a backend.",
        ],
    }


def model_registry_by_id() -> dict[str, ModelIntegrationRecord]:
    return {row.model_id: row for row in list_model_integrations()}
