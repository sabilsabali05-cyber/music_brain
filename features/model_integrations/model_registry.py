from features.model_integrations.model_integration_schema import ModelIntegrationRecord


def _record(
    *,
    model_id: str,
    family: str,
    role: str,
    inputs: list[str],
    outputs: list[str],
    tasks: list[str],
    personalization_path: str,
    safety_policy: str,
    training_allowed: bool = False,
    fine_tune_possible: bool = False,
    smoke_test_supported: bool = False,
    limitations: list[str] | None = None,
) -> ModelIntegrationRecord:
    return ModelIntegrationRecord(
        model_id=model_id,
        family=family,
        role_in_music_brain=role,
        input_modalities=inputs,
        output_modalities=outputs,
        expected_tasks=tasks,
        local_config_required=True,
        weights_required=True,
        training_allowed=training_allowed,
        fine_tune_possible=fine_tune_possible,
        user_personalization_path=personalization_path,
        safety_policy=safety_policy,
        availability_status="unavailable",
        unavailable_reason="disabled_by_default_until_explicitly_configured",
        smoke_test_supported=smoke_test_supported,
        limitations=limitations or ["Disabled by default.", "No implicit package installs or model downloads."],
    )


